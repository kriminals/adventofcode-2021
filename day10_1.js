let lines = input.split('\n').map(entry => entry.trim()).map(entry => entry.split(''))


function checkLine(line) {

    for(let index = 0; index < line.length ; index++) {
        let entry = line[index]
        if (entry == '>') {
            if (line[index - 1] !== '<') {
                return 25137
            }
            else {
                line.splice(index -1,2)
                return checkLine(line)
            }
        }
        else if (entry == ']') {
            if (line[index - 1] !== '[') {
                return 57
            }
            else {
                line.splice(index -1,2)
                return checkLine(line)
            }
        }
        else if (entry == ')') {
            if (line[index - 1] !== '(') {
                return 3
            }
            else {
                line.splice(index -1,2)
                return checkLine(line)
            }
        }
        else if (entry == '}') {
            if (line[index - 1] !== '{') {
                return 1197
            }
            else {
                line.splice(index -1,2)
                return checkLine(line)
            }
        }

        
    }
    return 0
}
let incompleteLines = lines.filter(line => checkLine(line) == 0)
function calculateScore(line) {
    let score = 0
    score = line.reduce((sum, char) => {
        let value
        switch (char) {
            case '(':
                value = 1
                break
            case '[':
                value = 2
                break
            case '{':
                value = 3
                break
            case '<':
                value = 4
                break
        }
        sum = sum * 5 + value
        return sum
    }, 0)
    return score
}
let totalScores = incompleteLines.map(line => line.reverse()).map(line => calculateScore(line)).sort((a,b) => a-b)
let middleScore = totalScores[(totalScores.length - 1) / 2]
